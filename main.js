let acc = document.getElementsByClassName("accordion");

for (let i = 0; i < acc.length; i++) {
  acc[i].addEventListener("click", function () {
    this.classList.toggle("active");
    let panel = this.nextElementSibling;
    if (panel.style.maxHeight) {
      panel.style.maxHeight = null;
    } else {
      panel.style.maxHeight = panel.scrollHeight + "px";
    }
  });
}

document.addEventListener("DOMContentLoaded", function () {
  let links = document.querySelectorAll("a[href^='#']");

  links.forEach(function (link) {
    link.addEventListener("click", function (event) {
      event.preventDefault();
      let targetId = this.getAttribute("href");
      scrollToSection(targetId);
    });
  });
});

function scrollToSection(sectionId) {
  let targetSection = document.querySelector(sectionId);
  if (targetSection) {
    let headerHeight = document.querySelector(".header").offsetHeight;
    let targetPosition = targetSection.offsetTop - headerHeight;
    window.scrollTo({
      top: targetPosition,
      behavior: "smooth",
    });
  }
}

function changeLanguage() { 
  var selectedLanguage = document.getElementById("language").value;
  localStorage.setItem("language", selectedLanguage);
  setQueryParameter('lang', selectedLanguage);
  applyTranslation(selectedLanguage);  
}

function setQueryParameter(param, value) {
  const urlParams = new URLSearchParams(window.location.search);
  urlParams.set(param, value);
  const newUrl = window.location.pathname + '?' + urlParams.toString();
  window.history.replaceState({}, '', newUrl);
}


function applyTranslation(language) {
  let listNeedToTranslate = document.querySelectorAll('[data-i18]');

  listNeedToTranslate.forEach((element) => {
      let translateKey = element.dataset.i18; 
      element.innerHTML = i18Obj[language][translateKey];
  }); 
}

function getLanguage() {
  const urlParams = new URLSearchParams(window.location.search);
  const selectedLanguage = urlParams.get('lang');
  if (selectedLanguage) {
    return selectedLanguage;
  } else {
    if(localStorage.getItem('language')) {
      return localStorage.getItem('language')
    } else {
      return 'en';
    }
  }
}

function initializePage() {
  const selectedLanguage = getLanguage();  
  applyTranslation(selectedLanguage);
  setQueryParameter('lang', selectedLanguage);
  const select = document.querySelector('select');
  select.value = selectedLanguage;
  const body = document.querySelector(".body-container");
  body.style.display = 'block';
  checkScreenSize();
}

function checkScreenSize() {
  const screenWidth = window.innerWidth;
 
  const elementsToHide = document.querySelectorAll('.hide-below-550px');

  if (screenWidth < 670) {
    elementsToHide.forEach(element => {
      element.style.display = 'none';
    });
  } else {
    elementsToHide.forEach(element => {
      element.style.display = 'flex'; 
    });
  }
}

window.addEventListener('load', initializePage);
window.addEventListener('resize', checkScreenSize);